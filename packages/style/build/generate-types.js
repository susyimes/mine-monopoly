function toCamelCase(str) {
  return str.replace(/-([a-z])/g, (_, letter) => letter.toUpperCase());
}

function generateTypes() {
  const lines = [];
  const varNameLines = [];

  lines.push('// Auto-generated by generate-types.js');
  lines.push('// @mine-monopoly/style TypeScript 类型定义');
  lines.push('');
  lines.push('export const FpColors = {');

  // 生成颜色类型
  for (const [key, value] of Object.entries(variableDefinitions.colors)) {
    const camelKey = toCamelCase(key);
    lines.push(`  ${camelKey}: '${value}',`);
    varNameLines.push(`  color${camelKey.charAt(0).toUpperCase() + camelKey.slice(1)}: '--fp-color-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');
  lines.push('export type FpColor = typeof FpColors[keyof typeof FpColors];');
  lines.push('');

  // 生成间距类型
  lines.push('export const FpSpacing = {');
  for (const [key, value] of Object.entries(variableDefinitions.spacing)) {
    lines.push(`  ${key}: '${value}',`);
    varNameLines.push(`  spacing${key.charAt(0).toUpperCase() + key.slice(1)}: '--fp-spacing-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');
  lines.push('export type FpSpacing = typeof FpSpacing[keyof typeof FpSpacing];');
  lines.push('');

  // 生成字体大小类型
  lines.push('export const FpFontSize = {');
  for (const [key, value] of Object.entries(variableDefinitions.fontSize)) {
    lines.push(`  ${key}: '${value}',`);
    varNameLines.push(`  fontSize${key.charAt(0).toUpperCase() + key.slice(1)}: '--fp-font-size-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');

  // 生成字重类型
  lines.push('export const FpFontWeight = {');
  for (const [key, value] of Object.entries(variableDefinitions.fontWeight)) {
    lines.push(`  ${key}: '${value}',`);
    varNameLines.push(`  fontWeight${key.charAt(0).toUpperCase() + key.slice(1)}: '--fp-font-weight-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');

  // 生成行高类型
  lines.push('export const FpLineHeight = {');
  for (const [key, value] of Object.entries(variableDefinitions.lineHeight)) {
    lines.push(`  ${key}: '${value}',`);
    varNameLines.push(`  lineHeight${key.charAt(0).toUpperCase() + key.slice(1)}: '--fp-font-line-height-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');

  // 生成阴影类型
  lines.push('export const FpShadows = {');
  for (const [key, value] of Object.entries(variableDefinitions.shadows)) {
    lines.push(`  ${key}: '${value}',`);
    varNameLines.push(`  shadow${key.charAt(0).toUpperCase() + key.slice(1)}: '--fp-shadow-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');

  // 生成层级类型
  lines.push('export const FpLayers = {');
  for (const [key, value] of Object.entries(variableDefinitions.layers)) {
    const camelKey = toCamelCase(key);
    lines.push(`  ${camelKey}: ${value},`);
    varNameLines.push(`  layer${camelKey.charAt(0).toUpperCase() + camelKey.slice(1)}: '--fp-layer-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');

  // 生成断点类型
  lines.push('export const FpBreakpoints = {');
  for (const [key, value] of Object.entries(variableDefinitions.breakpoints)) {
    lines.push(`  ${key}: '${value}',`);
    varNameLines.push(`  breakpoint${key.charAt(0).toUpperCase() + key.slice(1)}: '--fp-breakpoint-${key}',`);
  }
  lines.push('} as const;');
  lines.push('');

  // 生成 CSS 变量名映射
  lines.push('// CSS 变量名映射，用于 var() 函数');
  lines.push('export const FpVarNames = {');
  lines.push(...varNameLines);
  lines.push('} as const;');
  lines.push('');
  lines.push('export type FpVarName = typeof FpVarNames[keyof typeof FpVarNames];');
  lines.push('');

  // 生成工具函数类型
  lines.push('// 工具函数：获取 CSS 变量值');
  lines.push('export function fpVar(varName: FpVarName): string;');
  lines.push('export function fpVar(varName: FpVarName, fallback: string): string;');
  lines.push('');
  lines.push('// 工具函数：获取间距值');
  lines.push('export function fpSpacing(size: keyof typeof FpSpacing): string;');
  lines.push('');
  lines.push('// 工具函数：获取颜色值');
  lines.push('export function fpColor(color: keyof typeof FpColors): string;');
  lines.push('');

  const content = lines.join('\n');

  // 确保输出目录存在
  const distDir = path.join(__dirname, '../dist');
  if (!fs.existsSync(distDir)) {
    fs.mkdirSync(distDir, { recursive: true });
  }

  // 写入类型文件
  const typesPath = path.join(distDir, 'types.d.ts');
  fs.writeFileSync(typesPath, content, 'utf-8');

  console.log(`✓ 类型定义已生成: ${typesPath}`);
}

// 生成运行时辅助函数
function generateRuntime() {
  const lines = [];
  lines.push('/**');
  lines.push(' * @mine-monopoly/style 运行时辅助函数');
  lines.push(' */');
  lines.push('');
  lines.push("import { FpVarName, FpSpacing, FpColors } from './types';");
  lines.push('');
  lines.push('/** 获取 CSS 变量值 */');
  lines.push('export function fpVar(varName: FpVarName, fallback?: string): string {');
  lines.push('  return fallback ? `var(${varName}, ${fallback})` : `var(${varName})`;');
  lines.push('}');
  lines.push('');
  lines.push('/** 获取间距值 */');
  lines.push('export function fpSpacing(size: keyof typeof FpSpacing): string {');
  lines.push('  return `var(--fp-spacing-${size})`;');
  lines.push('}');
  lines.push('');
  lines.push('/** 获取颜色值 */');
  lines.push('export function fpColor(color: keyof typeof FpColors): string {');
  lines.push('  return `var(--fp-color-${color})`;');
  lines.push('}');
  lines.push('');

  const content = lines.join('\n');
  const distDir = path.join(__dirname, '../dist');
  const indexPath = path.join(distDir, 'index.js');
  fs.writeFileSync(indexPath, content, 'utf-8');

  console.log(`✓ 运行时辅助函数已生成: ${indexPath}`);
}

// 主函数
function main() {
  console.log('正在生成 @mine-monopoly/style 类型定义...\n');
  generateTypes();
  generateRuntime();
  console.log('\n✓ 生成完成!');
}

if (require.main === module) {
  main();
}

module.exports = { generateTypes, generateRuntime };
